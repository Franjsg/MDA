    <h1>¿Qué es Cortex?</h1>
    <p class="center-paragraph">
        Cortex es un proyecto surgido de la colaboración de una 
        neuropsicóloga y una fisioterapeuta con el que pretenden satisfacer 
        la demanda de profesionales con especialización en rehabilitación 
        neurológica en la isla de Gran Canaria. <br>
        El objetivo del proyecto es proporcionar a los pacientes un abordaje 
        transdisciplinar de calidad que se enfoque en la consecución de la 
        máxima autonomía y bienestar del paciente mediante un tratamiento 
        global, es decir, un tratamiento que abarque todas la esferas del 
        paciente: estado físico, interacción social y desempeño cognitivo y 
        emocional.</p>
